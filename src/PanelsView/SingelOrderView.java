    public void setCustomer(){
        jL_Name.setText("Name: "+orderLine.getCustomers().getName());
        jL_Adress.setText("Adress: "+orderLine.getCustomers().getStreet());
        jL_Cvr.setText("Cvr: "+orderLine.getCustomers().getCvrNumber());
        jL_Email.setText("Email: "+orderLine.getCustomers().getEmail());
        jL_Store.setText("Store: "+orderLine.getCustomers().getStoreChain());
        jL_Tlf.setText("Tlf: "+orderLine.getCustomers().getPhoneNumber());
    }
    
    public void setOrderContents(){
       OrderContentsButtom ocb;
       jP_orderContent.removeAll();
        for (int i = 0; i < orderLine.getAlbumList().size(); i++) {
            ocb = new OrderContentsButtom(orderLine.getAlbumList().get(i));
            jP_orderContent.add(ocb);
            ocb.setLocation(0, ((5 * i) + (ocb.getHeight() * i)));
            ocb.setVisible(true);
            int count = i+1;
            jP_orderContent.setPreferredSize(new Dimension(ocb.getWidth(), ((5 * count) + (ocb.getHeight() * count))));
        }
        jP_orderContent.revalidate();
        jP_orderContent.repaint();
    }
    
    public void checkStock(){
        for (AlbumToOrder ato : orderLine.getAlbumList()) {
            if (ato.getAmount() > ato.getAlbum().getStock()) {
                isIndStock = false;
                System.out.println(""+isIndStock);
            }
        }
        setButtom();
    }
    
    public void setButtom(){
        jB_OrderAccept.setEnabled(isIndStock);
    }
    
    public void updateStock(){
        for (AlbumToOrder al : orderLine.getAlbumList()) {
             albumHandler.updateStock(al.getAlbum(), -al.getAmount());
        }
        orderHandler.orderAcceptet(orderLine.getId());
        jFrame.dispose();
    }

